package com.arcus.javatraining.typecasting;

public class TypeCastDemo {

	public static void main(String[] args) {
		
		int i=10;
		byte b=120;
		
		//widening - smaller to bigger type
		int c= b;
		System.out.println(c);
		
		
		
		//converting int to byte, narrowing - bigger type to a smaller type
		
		byte bb=(byte) i;
	
		double mydouble=10;
		
		
		long longNum = 10;
		//int intNum = longNum;
		

	}

}
