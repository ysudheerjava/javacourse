package com.arcus.javatraining.operators.relational;

public class RelationalOperatorsExample {

	public static void main(String[] args) {

		int a =20;
		int b =10;
		
		//relational operator ==
		
		//returns true when both are equal
		System.out.println(a==b);
		
		//returns true when both values are not equal
		System.out.println(a!=b);
		
		System.out.println(a>b);
		System.out.println(a<b);
		
		System.out.println(a<=b);
		System.out.println(a>=b);
		
		
		

	}

}
